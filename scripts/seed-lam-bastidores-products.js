async function main() {
  const { data: categories, error: categoriesError } = await supabase
    .from('categories')
    .select('id, slug');

  if (categoriesError) {
    throw categoriesError;
  }

  const categoryMap = Object.fromEntries(categories.map((category) => [category.slug, category.id]));

  const payload = products.map((product) => ({
    category_id: categoryMap[product.categorySlug] ?? null,
    name: product.name,
    slug: product.slug,
    subtitle: product.subtitle,
    description: product.description,
    price_ars: product.price_ars,
    price_usd: null,
    stock: product.stock,
    max_per_order: product.max_per_order,
    image_url: null,
    is_active: true,
    is_featured: product.is_featured ?? false,
    product_type: product.product_type,
    event_date: null,
    event_location: COMMON_LOCATION,
    badge: product.badge,
    features: product.features,
    cta_link: null,
  }));

  const { data, error } = await supabase
    .from('products')
    .upsert(payload, { onConflict: 'slug' })
    .select('name, slug, price_ars, stock, product_type');

  if (error) {
    throw error;
  }

  console.log(`Productos cargados/actualizados: ${data.length}`);
  console.table(data);
}

main().catch((error) => {
  console.error('Error al cargar productos LAM Bastidores:', error);
  process.exit(1);
});
